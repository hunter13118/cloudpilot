// ──────────── Deployment simulation (Flight Deck) ────────────
const STEP_PLAN = [
  ["preflight-policy", "Policy gate · org constraints replay", "PREFLIGHT", 2.0],
  ["preflight-plan", "terraform plan · drift check", "PREFLIGHT", 3.0],
  ["ignition-build", "Cloud Build · container bake", "IGNITION", 4.0],
  ["ignition-scan", "Artifact scan · CVE sweep", "IGNITION", 2.5],
  ["ascent-apply", "terraform apply · provisioning", "ASCENT", 5.0],
  ["ascent-smoke", "Smoke probes · golden signals", "ASCENT", 2.5],
  ["orbit-traffic", "Traffic shift · 100% to new revision", "ORBIT", 2.0],
];
const DEPLOYMENTS = new Map();

// Tests compress the timeline via globalThis.__CLOUDPILOT_SPEED (e.g. 0.12).
const speed = () => (typeof globalThis !== "undefined" && globalThis.__CLOUDPILOT_SPEED) || 1;

export function startDeployment(runId, { chaos = false } = {}) {
  const id = `dep-${Math.random().toString(36).slice(2, 10)}`;
  DEPLOYMENTS.set(id, { t0: Date.now() / 1000, runId, failStep: chaos ? "ascent-apply" : null, failedAt: null });
  return getDeployment(id);
}

export function retryDeployment(id) {
  const dep = DEPLOYMENTS.get(id);
  if (!dep) throw new Error("unknown deployment");
  dep.failStep = null;
  const sp = speed();
  let elapsed = 0;
  for (const [sid, , , dur] of STEP_PLAN) {
    elapsed += dur * sp;
    if (sid === dep.failedAt) break;
  }
  dep.t0 = Date.now() / 1000 - elapsed + 0.5 * sp;
  return getDeployment(id);
}

export function getDeployment(id) {
  const dep = DEPLOYMENTS.get(id);
  if (!dep) throw new Error("unknown deployment");
  const sp = speed();
  const elapsed = Date.now() / 1000 - dep.t0;
  const steps = [];
  const logTail = [];
  let cursor = 0, phase = "PREFLIGHT", halted = false;
  const total = STEP_PLAN.reduce((a, s) => a + s[3], 0) * sp;
  for (const [sid, name, ph, dur] of STEP_PLAN) {
    const d = dur * sp;
    let status;
    if (halted) status = "pending";
    else if (elapsed >= cursor + d) {
      if (dep.failStep === sid) { status = "failed"; halted = true; dep.failedAt = sid; logTail.push(`[${sid}] ERROR: step failed — see diagnosis`); }
      else { status = "success"; logTail.push(`[${sid}] ✓ ${name} (${dur.toFixed(1)}s)`); }
    } else if (elapsed >= cursor) { status = "running"; phase = ph; logTail.push(`[${sid}] … ${name}`); }
    else status = "pending";
    steps.push({ id: sid, name, phase: ph, status, duration_ms: status === "success" ? Math.round(dur * 1000) : null, log: null });
    cursor += d;
  }
  const done = steps.filter((s) => s.status === "success").length;
  let progress = halted ? Math.round((100 * done) / STEP_PLAN.length) : Math.round((100 * Math.min(elapsed, total)) / total);
  if (steps.every((s) => s.status === "success")) { phase = "ORBIT"; progress = 100; logTail.push("[orbit] 🛰  Architecture is live. Telemetry nominal."); }
  else if (halted) phase = steps.find((s) => s.status === "failed").phase;
  return { deployment_id: id, phase, progress, steps, log_tail: logTail.slice(-8) };
}

